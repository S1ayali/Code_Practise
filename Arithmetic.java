class Arithmetic
{
	public static void main(String [] args)
	{
		int a=49 , b=34;
		System.out.println("Addition = "+(a+b));
		System.out.println("Substraction = " +(a-b));
		System.out.println("Multiplicatin = " +(a*b));
		System.out.println("Division = " +(a/b));
		System.out.println("Modulo = " +(a%b));

	}
} 

